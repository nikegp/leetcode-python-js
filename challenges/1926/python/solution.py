#!/usr/bin/env python
from collections import defaultdict, deque
from typing import List

from challenges.utils import expect


class Solution:
    def nearestExit(self, maze: List[List[str]], entrance: List[int]) -> int:
        entrance_y, entrance_x = entrance
        max_y = len(maze) - 1
        max_x = len(maze[0]) - 1

        cells = defaultdict(dict)
        for y, line in enumerate(maze):
            for x, point in enumerate(line):
                if point == ".":
                    if (y == max_y or x == max_x or x == 0 or y == 0) and not (
                        y == entrance_y and x == entrance_x
                    ):
                        # it's an exit!
                        cells[x][y] = 2
                    else:
                        # it's an empty cell
                        cells[x][y] = 1

        queue = deque([(entrance_x, entrance_y, 0)])
        visited = {entrance_x, entrance_y}
        while queue:
            item_x, item_y, step = queue.popleft()

            for change_x, change_y in [(+1, 0), (-1, 0), (0, +1), (0, -1)]:
                new_x = item_x + change_x
                new_y = item_y + change_y

                cell = cells[new_x].get(new_y)
                if cell == 2:
                    return step + 1
                if cell == 1:
                    if (new_x, new_y) not in visited:
                        visited.add((new_x, new_y))
                        queue.append((new_x, new_y, step + 1))

        return -1


